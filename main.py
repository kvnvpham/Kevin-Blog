def clean_text(content):
    allowed_tags = ['a', 'abbr', 'acronym', 'b', 'blockquote', 'br', 'code', 'dd', 'del', 'div', 'dl', 'dt', 'em', 'h1',
                    'h2', 'h3', 'h4', 'h5', 'h6', 'hr', 'i', 'img', 'li', 'ol', 'p', 'pre', 's', 'span', 'strong',
                    'sub', 'sup', 'table', 'tbody', 'td', 'tfoot', 'th', 'thead', 'tr', 'ul'
                    ]
    allowed_attrs = {'*': ['class'],
                     'a': ['href', 'rel'],
                     'img': ['src', 'alt', 'style']
                     }
    cleaned_text = bleach.clean(content, tags=allowed_tags, attributes=allowed_attrs)
    return cleaned_text


def admin_only(function):
    @wraps(function)
    def decorated_function(*args, **kwargs):
        if current_user.id != 1:
            return abort(403)
        return function(*args, **kwargs)
    return decorated_function


